    {
        $organizations = Organization::where('status', Organization::STATUS_ACTIVE)->get();
        $requests = Requests::with('organization:id,name')
            ->with('student')
            ->with('employee')
            ->with('guardian')
            ->take(10)
            ->latest()
            ->get();
        // dd($requests->toArray());
        return view('request.index', [
            'organizations' => $organizations,
            'requests' => $requests,
        ]);
    }

